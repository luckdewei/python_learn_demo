# 同位置的元素类型相同可比较
print([1, "apple"] < [2, "banana"])

# 列表间的关系比较常用于对序列排序、查找最大/最小值、版本号比较等实际场景。
# 嵌套列表按第一层逐项比较，相同则继续比更深一层。

v1 = "3.9.1".split(".")
v2 = "3.10.0".split(".")
print([int(x) for x in v1] < [int(x) for x in v2])  # True


